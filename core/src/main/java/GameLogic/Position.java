package GameLogic;

public class Position {

    private final int x;
    private final int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position newPosition(int dx, int dy) {
        return new Position(x + dx, y + dy);
    }
}

